public class PersonaInfo {
    public static void main(String[] args) {
        // Declaración y asignación de variables
        int edad = 20;                 // Edad de la persona
        double altura = 1.75;          // Altura en metros
        char inicialNombre = 'J';      // Inicial del nombre
        boolean esMayorDeEdad = edad >= 18; // Verifica si es mayor de edad

        // Imprimir los valores
        System.out.println("Edad: " + edad + " años");
        System.out.println("Altura: " + altura + " metros");
        System.out.println("Inicial del nombre: " + inicialNombre);
        System.out.println("¿Es mayor de edad?: " + esMayorDeEdad);
    }
}
